verse-7
BG 18.7: Prescribed duties should never be renounced. Such deluded renunciation is said to be in the mode of ignorance.
verse-7

verse-8
दु:खमित्येव यत्कर्म कायक्लेशभयात्यजेत् |
स कृत्वा राजसं त्यागं नैव त्यागफलं लभेत् || 8||
verse-8
