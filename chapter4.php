verse-21
BG 4.21: Free from expectations and the sense of ownership, with mind and intellect fully controlled, they incur no sin, even though performing actions by one’s body.
verse-21

verse-22
यदृच्छालाभसन्तुष्टो द्वन्द्वातीतो विमत्सर: |
सम: सिद्धावसिद्धौ च कृत्वापि न निबध्यते || 22||

verse-22
yadṛichchhā-lābha-santuṣhṭo dvandvātīto vimatsaraḥ
