verse-19
BG 8.19: The multitudes of beings repeatedly take birth with the advent of Brahma’s day, and are reabsorbed on the arrival of the cosmic night, to manifest again automatically on the advent of the next cosmic day.
verse-19

verse-20
परस्तस्मात्तु भावोऽन्योऽव्यक्तोऽव्यक्तात्सनातन: |
