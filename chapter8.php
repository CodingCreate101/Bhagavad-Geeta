verse-14
BG 8.14: O Parth, for those yogis who always think of me with exclusive devotion, I am easily attainable because of their constant absorption in me.
verse-14

verse-15
मामुपेत्य पुनर्जन्म दु:खालयमशाश्वतम् |
नाप्नुवन्ति महात्मान: संसिद्धिं परमां गता: || 15||
verse-15
mām upetya punar janma duḥkhālayam aśhāśhvatam
nāpnuvanti mahātmānaḥ sansiddhiṁ paramāṁ gatāḥ
