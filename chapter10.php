<h1>Chapter 10: Vibhūti Yoga</h1>

verse-1
श्रीभगवानुवाच |
भूय एव महाबाहो शृणु मे परमं वच: |
यत्तेऽहं प्रीयमाणाय वक्ष्यामि हितकाम्यया || 1||
verse-1
